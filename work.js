// func1
function add(a, b, c, d) {
    return a + b + c + d;
  }
  
  // func2
  function sub(a, b, c, d) {
    return a - b - c - d;
  }
  
  // func3
  function mult(a, b, c, d) {
    return a * b * c * d;
  }
  
  // func4
  function div(a, b, c, d) {
    return a / b / c / d;
  }
  